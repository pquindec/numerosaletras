    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(textnumero.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese un numero");
            return; // aqui termina la ejecuci√≥n cuando no ingresas un digito
        }
        
        String nu = (textnumero.getText());
        
        try {
            Integer.parseInt(nu);// Intenta parsear a entero, y se va a caer
        } catch (Exception e) {
            // cuando se cae entra aqui y muestra el dialog y retorna
            JOptionPane.showMessageDialog(null, "Ingrese un numero");
            textnumero.setText("");
            return;
        }
        
        metodo m = new metodo();
        
         if ( Integer.parseInt(nu) < 0 || Integer.parseInt(nu) > 1000000000){
            mostrar.setText("Ingrese un numero valido");
        }
         else mostrar.setText(m.cantidadConLetra(nu));
    }//GEN-LAST:event_jButton1ActionPerformed

